package hk.edu.cuhk.ie.iems5722.a2_1155152636;

public class Room {
    private int id;
    private String name;

    public Room(int id, String name)
    {
        this.id = id;
        this.name = name;
    }

    public int getId()
    {
        return id;
    }

    public String getName()
    {
        return name;
    }
}
